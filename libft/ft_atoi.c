#include "libft.h"

int	ft_atoi(const char *str)
{
	int	final;
	int	sign;
	int	i;

	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	sign = 1;
	if ((str[i] == '+') || (str[i] == '-'))
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	final = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		final = final * 10 + sign * (str[i] - 48);
		i++;
	}
	return (final);
}
